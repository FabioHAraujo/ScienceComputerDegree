    private void tcuListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tcuListaMouseClicked
        DefaultTableModel model = (DefaultTableModel) tcuMostra.getModel();
        model.setRowCount(0);

        usuarios = (List<Usuario>) carregarDados();

        for (Usuario usuario : usuarios) {
            Object[] row = {usuario.getCadastro(), usuario.getUsuario(), usuario.getNome(), usuario.getSobrenome(), usuario.getEmail()};
            model.addRow(row);
        }
    }//GEN-LAST:event_tcuListaMouseClicked

