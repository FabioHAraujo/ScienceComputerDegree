    private void tcuDeletaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tcuDeletaActionPerformed

        // Verificar se há elementos na lista
    if (!listaDePessoas.isEmpty()) {
        // Remover o objeto atual da lista
        listaDePessoas.remove(registroAtual);
        
        // Atualizar o valor do registroAtual
        if (registroAtual >= listaDePessoas.size()) {
            registroAtual = listaDePessoas.size() - 1;
        }
        
        // Atualizar os campos com o objeto atual, se houver
        if (registroAtual >= 0) {
            Pessoa pessoaAtual = listaDePessoas.get(registroAtual);
            tfNome.setText(pessoaAtual.getNome());
            tfIdade.setText(Integer.toString(pessoaAtual.getIdade()));
            tfEndereco.setText(pessoaAtual.getEndereco());
        } else {
            // Limpar os campos se a lista estiver vazia
            tfNome.setText("");
            tfIdade.setText("");
            tfEndereco.setText("");
        }
        
        // Atualizar a exibição da contagem e quantidade de registros
        tfContagem.setText(Integer.toString(registroAtual + 1));
        JOptionPane.showMessageDialog(this, "Registro removido com sucesso.");
    } else {
        JOptionPane.showMessageDialog(this, "Não há registros para remover.");
    }
    }//GEN-LAST:event_tcuDeletaActionPerformed

