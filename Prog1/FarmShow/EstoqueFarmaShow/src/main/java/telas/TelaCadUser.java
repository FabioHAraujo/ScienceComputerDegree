    private void tcuCadastraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tcuCadastraMouseClicked
        String nome = tcuNome.getText();
        String sobrenome = tcuSobrenome.getText();
        String email = tcuEmail.getText();
        String usuario = tcuUser.getText();
        String senha = new String(tcuPass.getPassword());
        String CPF = tcuCPF.getText();

        Usuario usuarioObj = new Usuario();
        usuarioObj.setNome(nome);
        usuarioObj.setSobrenome(sobrenome);
        usuarioObj.setEmail(email);
        usuarioObj.setUsuario(usuario);
        usuarioObj.setSenha(senha);
        usuarioObj.setCpf(CPF);

        // Adicione o usuário à lista existente
        File arquivo = new File("dados.obj");
        arquivo.delete();
        usuarios.add(usuarioObj);

        // Salva a lista atualizada no arquivo binário
        salvarDados(usuarios);
        
        DefaultTableModel model = (DefaultTableModel) tcuMostra.getModel();
        model.setRowCount(0);

        for (Usuario user : usuarios) {
            Object[] row = {user.getCadastro(), user.getUsuario(), user.getNome(), user.getSobrenome(), user.getEmail()};
            model.addRow(row);
        }


    // Restante do código aqui, se necessário

    
    }//GEN-LAST:event_tcuCadastraMouseClicked

