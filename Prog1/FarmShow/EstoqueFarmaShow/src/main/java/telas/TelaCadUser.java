    private void tcuDeletaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tcuDeletaActionPerformed

        DefaultTableModel model = (DefaultTableModel) tcuMostra.getModel();
        int selectedRow = tcuMostra.getSelectedRow();

        if (selectedRow != -1) {
            int cadastro = (int) model.getValueAt(selectedRow, 0);
            Usuario usuarioObj = null; // Declare a variável usuarioObj

            for (Usuario usuario : usuarios) {
                if (usuario.getCadastro() == cadastro) {
                    usuarioObj = usuario;
                    break;
                }
            }

            if (usuarioObj != null) {
                boolean excluido = Usuario.excluirUsuario(usuarioObj.getCadastro());

                if (excluido) {
                    model.removeRow(selectedRow);

                    // Crie uma lista temporária para armazenar os usuários atualizados
                    List<Usuario> listaTemporaria = new ArrayList<>(usuarios);

                    // Remove o usuário da lista temporária
                    listaTemporaria.remove(usuarioObj);

                    // Deleta o arquivo antigo
                    File arquivo = new File("dados.obj");
                    arquivo.delete();

                    // Salva a lista atualizada no arquivo binário
                    salvarDados(listaTemporaria);

                    // Atualiza a lista de usuários com a lista atualizada
                    usuarios = carregarDados();
                } else {
                    // Trate o caso de falha na exclusão, se necessário
                }
            } else {
                // Trate o caso de usuário não encontrado, se necessário
            }
        } else {
            // Nenhuma linha selecionada. Trate esse caso de acordo com a lógica do seu aplicativo.
        }
    }//GEN-LAST:event_tcuDeletaActionPerformed

